interface PanelProps {
  title?: string;
  children?: React.ReactNode;
}

export default function Panel({ title, children }: PanelProps) {
  const [drawerOpen, setDrawerOpen] = useState(() => {
    if (typeof window !== "undefined") {
      const saved = localStorage.getItem(DRAWER_STORAGE_KEY);
      return saved !== null ? saved === "true" : true;
    }
    return true;
  });

  useEffect(() => {
    localStorage.setItem(DRAWER_STORAGE_KEY, String(drawerOpen));
  }, [drawerOpen]);

  return (
    <div className="drawer lg:drawer-open">
      <input
        id="my-drawer-4"
        type="checkbox"
        className="drawer-toggle"
        checked={drawerOpen}
        onChange={(e) => setDrawerOpen(e.target.checked)}
      />
      <div className="drawer-content">
        <Navbar title={title} />
        <div className="p-4">{children}</div>
      </div>

      <div className="drawer-side is-drawer-close:overflow-visible">
        <label htmlFor="my-drawer-4" aria-label="close sidebar" className="drawer-overlay"></label>
        <div className="flex min-h-full flex-col items-start bg-base-200 is-drawer-close:w-14 is-drawer-open:w-32">
          <ul className="menu w-full grow">
            {menuItems.map((item) => (
              <li key={item.label}>
                <a
                  href={item.href}
                  className="is-drawer-close:tooltip is-drawer-close:tooltip-right"
                  data-tip={item.label}
                >
                  {item.icon}
                  <span className="is-drawer-close:hidden">{item.label}</span>
                </a>
              </li>
            ))}
          </ul>
          <div className="p-3">
            <ThemeController />
          </div>
        </div>
      </div>
    </div>
  );
}